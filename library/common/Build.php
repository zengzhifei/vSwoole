        ],
        'Task'         => [
            'annotation' => '异步任务执行回调函数',
            'parameters' => ['\swoole_server $server', 'int $task_id', 'int $src_worker_id', 'mixed $data']
        ],
        'Finish'       => [
            'annotation' => '异步任务执行完成回调函数',
